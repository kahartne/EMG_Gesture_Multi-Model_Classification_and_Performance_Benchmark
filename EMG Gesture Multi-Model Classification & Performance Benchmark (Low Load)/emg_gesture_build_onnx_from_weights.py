import numpy as np
import onnx
from onnx import helper, TensorProto

# Load trained parameters
W = np.loadtxt("models/logistic_weights.txt").astype(np.float32).T
b = np.loadtxt("models/logistic_bias.txt").astype(np.float32)

print("W shape:", W.shape)
print("Expected input features:", W.shape[0])

num_features, num_classes = W.shape

# Inputs
input_tensor = helper.make_tensor_value_info("input", TensorProto.FLOAT, [None, W.shape[0]])
output_tensor = helper.make_tensor_value_info("output", TensorProto.INT64, [None])

# Initializers
W_initializer = helper.make_tensor("W", TensorProto.FLOAT, W.shape, W.flatten())
b_initializer = helper.make_tensor("b", TensorProto.FLOAT, b.shape, b)

# Nodes
matmul = helper.make_node("MatMul", ["input", "W"], ["z"])
add = helper.make_node("Add", ["z", "b"], ["z_bias"])
argmax = helper.make_node("ArgMax", ["z_bias"], ["output"], axis=1, keepdims=0)

# Graph
graph = helper.make_graph(
    [matmul, add, argmax],
    "LogisticRegressionManual",
    [input_tensor],
    [output_tensor],
    [W_initializer, b_initializer]
)

# Model
model = helper.make_model(graph, opset_imports=[helper.make_opsetid("ai.onnx", 13)])
model.ir_version = onnx.IR_VERSION
onnx.save(model, "models/logistic_regression.onnx")

print("Saved clean ONNX model (no ZipMap)")