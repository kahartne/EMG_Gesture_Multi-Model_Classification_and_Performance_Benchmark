import numpy as np
import onnx
from onnx import helper, TensorProto

# Load weights
W0 = np.loadtxt("models/mlp_W0.txt").astype(np.float32)
b0 = np.loadtxt("models/mlp_b0.txt").astype(np.float32)

W1 = np.loadtxt("models/mlp_W1.txt").astype(np.float32)
b1 = np.loadtxt("models/mlp_b1.txt").astype(np.float32)

W2 = np.loadtxt("models/mlp_W2.txt").astype(np.float32)
b2 = np.loadtxt("models/mlp_b2.txt").astype(np.float32)

print("W0:", W0.shape)
print("W1:", W1.shape)
print("W2:", W2.shape)

# I/O
input_tensor = helper.make_tensor_value_info("input", TensorProto.FLOAT, [None, W0.shape[0]])
output_tensor = helper.make_tensor_value_info("z_out", TensorProto.FLOAT, [None, W1.shape[1]])

# Initializers
init = [
  helper.make_tensor("W0", TensorProto.FLOAT, W0.shape, W0.flatten()),
  helper.make_tensor("b0", TensorProto.FLOAT, b0.shape, b0),
  helper.make_tensor("W1", TensorProto.FLOAT, W1.shape, W1.flatten()),
  helper.make_tensor("b1", TensorProto.FLOAT, b1.shape, b1),
  helper.make_tensor("W2", TensorProto.FLOAT, W2.shape, W2.flatten()),
  helper.make_tensor("b2", TensorProto.FLOAT, b2.shape, b2),
]

# Nodes
nodes = [
    # Layer 0
    helper.make_node("MatMul", ["input", "W0"], ["z0"]),
    helper.make_node("Add", ["z0", "b0"], ["a0"]),
    helper.make_node("Relu", ["a0"], ["h0"]),

    # Layer 1
    helper.make_node("MatMul", ["h0", "W1"], ["z1"]),
    helper.make_node("Add", ["z1", "b1"], ["a1"]),
    helper.make_node("Relu", ["a1"], ["h1"]),

    # Layer 2 (FINAL OUTPUT)
    helper.make_node("MatMul", ["h1", "W2"], ["z2"]),
    helper.make_node("Add", ["z2", "b2"], ["z_out"]),
]

graph = helper.make_graph(nodes, "MLP_2_HIDDEN", [input_tensor], [output_tensor], init)
model = helper.make_model(graph, opset_imports=[helper.make_operatorsetid("ai.onnx", 13)])
model.ir_version = onnx.IR_VERSION

onnx.save(model, "models/mlp.onnx")

print("Saved MLP ONNX model")