import matplotlib.pyplot as plt
import re

def parse_nvprof(file_path):
  kernels = []
  times = []

  with open(file_path, "r") as f:
      lines = f.readlines()

  for line in lines:
      # Match lines with kernel timing
      match = re.search(r"\s+([\d\.]+)%\s+([\d\.]+[a-z]+)\s+.*\s+(.+)", line)
      if match:
          percent = float(match.group(1))
          time = match.group(2)
          name = match.group(3).strip()

          # Convert time to ms
          if "ms" in time:
              t = float(time.replace("ms", ""))
          elif "us" in time:
              t = float(time.replace("us", "")) / 1000
          elif "s" in time:
              t = float(time.replace("s", "")) * 1000
          else:
              continue

          kernels.append(name)
          times.append(t)

  return kernels, times


def plot_kernels(file_path, title, output):
  kernels, times = parse_nvprof(file_path)

  # Take top 10 kernels
  combined = list(zip(kernels, times))
  combined.sort(key=lambda x: x[1], reverse=True)
  combined = combined[:10]

  kernels, times = zip(*combined)

  plt.figure()
  plt.barh(kernels, times)
  plt.xlabel("Time (ms)")
  plt.title(title)
  plt.gca().invert_yaxis()
  plt.tight_layout()
  plt.savefig(output)
  plt.show()

"""Kernel Plots"""

plot_kernels(
    "results/nvprof_gpu_fp32.txt",
    "Top GPU Kernels (FP32)",
    "results/kernels_fp32.png"
)

plot_kernels(
    "results/nvprof_gpu_int8.txt",
    "Top GPU Kernels (INT8)",
    "results/kernels_int8.png"
)