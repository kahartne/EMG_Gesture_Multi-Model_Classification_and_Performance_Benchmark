import numpy as np
import time
import matplotlib.pyplot as plt
import os

N = 1_000_000
x = np.random.rand(N)

"""Scalar"""

# SCALAR

start = time.perf_counter()

y = np.empty_like(x)
for i in range(N):
  y[i] = x[i] * 2 + 1

scalar_time = time.perf_counter() - start
print(f"Scalar time: {scalar_time:.6f}s")

"""Vector & Speedup"""

# SIMD (no O3, but vectorization should be sufficient)

start = time.perf_counter()

y = x * 2 + 1

simd_time = time.perf_counter() - start
print(f"SIMD (NumPy) time: {simd_time:.6f}s")
print(f"Speedup: {scalar_time / simd_time:.2f}x")

with open("results/simd_results.txt", "w") as f:
    f.write(f"Scalar time: {scalar_time:.6f}s\n")
    f.write(f"SIMD time: {simd_time:.6f}s\n")
    f.write(f"Speedup: {scalar_time / simd_time:.2f}x\n")

"""SIMD Chart"""

# Ensure results folder exists
os.makedirs("results", exist_ok=True)

print("Saved SIMD bar chart to results/simd_bar_chart.png")

# Data
labels = ["Scalar", "SIMD (NumPy)"]
times = [scalar_time, simd_time]

x = np.arange(len(labels))

fig, ax = plt.subplots(figsize=(6, 5))

bars = ax.bar(x, times)

# Titles and labels (match your other figures)
ax.set_title("SIMD vs Scalar Execution Time")
ax.set_xlabel("Method")
ax.set_ylabel("Time (seconds)")

# X ticks
ax.set_xticks(x)
ax.set_xticklabels(labels, rotation=15)

# Add value labels
for i, v in enumerate(times):
    ax.text(i, v, f"{v:.4f}", ha='center', va='bottom')

# Y-axis padding
ax.set_ylim(0, max(times) * 1.15)

plt.tight_layout()
plt.savefig("results/simd_bar_chart.png")
plt.show()
plt.close()

"""Speedup Chart"""

speedup = scalar_time / simd_time

labels = ["Scalar (baseline)", "SIMD (NumPy)"]
speedups = [1.0, speedup]

x = np.arange(len(labels))

fig, ax = plt.subplots(figsize=(6, 5))

bars = ax.bar(x, speedups)

ax.set_title("Relative Performance: SIMD vs Scalar")
ax.set_xlabel("Method")
ax.set_ylabel("Relative Speed (x)")

ax.set_xticks(x)
ax.set_xticklabels(labels, rotation=15)

# Add value labels
for i, v in enumerate(speedups):
    ax.text(i, v, f"{v:.2f}x", ha='center', va='bottom')

ax.set_ylim(0, max(speedups) * 1.15)

plt.tight_layout()
plt.savefig("results/simd_speedup_chart.png")
plt.show()
plt.close()