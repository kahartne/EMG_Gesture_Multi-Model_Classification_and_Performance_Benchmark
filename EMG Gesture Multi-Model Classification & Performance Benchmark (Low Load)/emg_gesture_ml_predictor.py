import onnxruntime as ort
import os, shutil, zipfile
from skl2onnx import convert_sklearn
from skl2onnx.common.data_types import FloatTensorType
import onnx

EXPORT_DIR = "models"
os.makedirs(EXPORT_DIR, exist_ok=True)

print("Export directory:", EXPORT_DIR)

# Define ONNX input shape
initial_type = [('input', FloatTensorType([None, x_train.shape[1]]))]

def save_model_onnx(model, name):
    out = f"{EXPORT_DIR}/{name}.onnx"

    try:
        initial_type = [('input', FloatTensorType([None, 3]))]
        onnx_model = convert_sklearn(
            model,
            initial_types=initial_type,
            options={id(model): {"zipmap": False}},
            target_opset=13)
        for opset in onnx_model.opset_import:
            if opset.domain == "":
                opset.domain = "ai.onnx"

        with open(out, "wb") as f:
            f.write(onnx_model.SerializeToString())
        print("Saved ONNX:", out)
    except Exception as e:
        print(f"[ERROR] Could not convert {name} to ONNX:", e)

# Export models (ONNX)
save_model_onnx(log_model, "logistic_regression")
save_model_onnx(mlp_model, "mlp")

print("\nAll ONNX files exported.\n")

# Save weights + bias
