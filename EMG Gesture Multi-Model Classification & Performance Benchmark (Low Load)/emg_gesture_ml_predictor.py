def validate_onnx(path, y_true, y_pred_sklearn):
  session = ort.InferenceSession(path)
  input_name = session.get_inputs()[0].name

  out = session.run(None, {input_name: x_test.astype("float32")})[0]

  if len(out.shape) > 1:
      pred = out.argmax(axis=1)
  else:
      pred = out.flatten()

  return np.all(pred == y_pred_sklearn)

