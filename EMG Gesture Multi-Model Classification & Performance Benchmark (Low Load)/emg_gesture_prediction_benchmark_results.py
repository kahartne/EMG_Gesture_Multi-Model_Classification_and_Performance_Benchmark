import pandas as pd
import matplotlib.pyplot as plt

# Load results
df = pd.read_csv("results/benchmark_results.csv")

# Clean model names
df["Model"] = df["Model"].str.replace(".onnx", "", regex=False)

# Separate models
df_log = df[df["Model"].str.contains("logistic")]
df_mlp = df[df["Model"].str.contains("mlp")]

# Create combined label
df["Config"] = df["Device"].str.upper() + " " + df["Precision"]

"""Execution Time Plot"""

plt.figure(figsize=(12, 6))

for model in df["Model"].unique():
  subset = df[df["Model"] == model]

  plt.bar(
      subset["Config"] + f"\n({model})",
      subset["Total Time (ms)"]
  )

plt.xticks(rotation=45)
plt.xlabel("Configuration")
plt.ylabel("Execution Time (ms)")
plt.title("Inference Time Comparison")
plt.xticks(rotation=30, ha='right')
plt.tight_layout()
plt.savefig("results/time_comparison.png")
plt.show()

"""Speedup vs CPU"""

speedup_rows = []

df["BaseModel"] = df["Model"].str.replace("_int8", "", regex=False)

for model in df["BaseModel"].unique():
  model_df = df[df["BaseModel"] == model]

  cpu_rows = model_df[
    (model_df["Device"].str.lower() == "cpu") &
    (model_df["Precision"].str.upper() == "FP32")]

  if len(cpu_rows) == 0:
    print(f"[WARNING] No CPU FP32 baseline found for model: {model}")
    continue

  cpu_time = cpu_rows["Total Time (ms)"].values[0]

  for _, row in model_df.iterrows():
    speedup = cpu_time / row["Total Time (ms)"]
    speedup_rows.append({
        "Model": model,
        "Config": f"{row['Device'].upper()} {row['Precision']} ({model})",
        "Speedup": speedup
    })

speedup_df = pd.DataFrame(speedup_rows)

plt.figure(figsize=(12, 6))

for model in speedup_df["Model"].unique():
  subset = speedup_df[speedup_df["Model"] == model]

  plt.bar(
      subset["Config"],
      subset["Speedup"]
  )

plt.xticks(rotation=45)
plt.xlabel("Configuration")
plt.ylabel("Speedup (vs CPU)")
plt.title("Speedup Comparison")
plt.xticks(rotation=30, ha='right')
plt.tight_layout()
plt.savefig("results/speedup.png")
plt.show()

"""Throughput Plot"""

plt.figure(figsize=(12, 6))

for model in df["Model"].unique():
  subset = df[df["Model"] == model]

  plt.bar(
      subset["Config"] + f"\n({model})",
      subset["Throughput (samples/sec)"]
  )

plt.xticks(rotation=45)
plt.xlabel("Configuration")
plt.ylabel("Throughput (samples/sec)")
plt.title("Throughput Comparison")
plt.xticks(rotation=30, ha='right')
plt.tight_layout()
plt.savefig("results/throughput.png")
plt.show()

"""Transfer Time Plot"""

plt.figure(figsize=(12, 6))

for model in df["Model"].unique():
  subset = df[df["Model"] == model]

  plt.bar(
      subset["Config"] + f"\n({model})",
      subset["Transfer Time (ms)"]
  )

plt.xticks(rotation=45)
plt.xlabel("Configuration")
plt.ylabel("Transfer Time (ms)")
plt.title("Transfer Overhead Comparison")
plt.xticks(rotation=30, ha='right')
plt.tight_layout()
plt.savefig("results/transfer_time.png")
plt.show()

"""Load vs Inference"""

plt.figure(figsize=(12, 6))

labels = []
load_vals = []
inf_vals = []

for _, row in df.iterrows():
  label = f"{row['Config']}\n({row['Model']})"
  labels.append(label)
  load_vals.append(row["Load Time (ms)"])
  inf_vals.append(row["Inference Time (ms)"])

plt.bar(labels, load_vals, label="Load Time")
plt.bar(labels, inf_vals, bottom=load_vals, label="Inference Time")

plt.xticks(rotation=45)
plt.xlabel("Configuration")
plt.ylabel("Time (ms)")
plt.title("Load vs Inference Time Breakdown")
plt.legend(loc="upper left", bbox_to_anchor=(1, 1))
plt.xticks(rotation=30, ha='right')
plt.tight_layout()
plt.savefig("results/load_vs_inference.png")
plt.show()

"""MLP vs Logsitic Regression"""

# Log
df_log.pivot(index="Device", columns="Precision", values="Total Time (ms)").plot(kind="bar")
plt.title("Logistic Regression: FP32 vs INT8")
plt.ylabel("Time (ms)")
plt.savefig("results/logistic_time.png")
plt.clf()

# MLP
df_mlp.pivot(index="Device", columns="Precision", values="Total Time (ms)").plot(kind="bar")
plt.title("MLP: FP32 vs INT8")
plt.ylabel("Time (ms)")
plt.savefig("results/mlp_time.png")
plt.clf()

# FP32
df_fp32 = df[df["Precision"] == "FP32"]

df_fp32.pivot(index="Device", columns="Model", values="Total Time (ms)").plot(kind="bar")
plt.title("FP32: Logistic vs MLP")
plt.ylabel("Time (ms)")
plt.savefig("results/fp32_model_comparison.png")
plt.clf()

# INT8
df_int8 = df[df["Precision"] == "INT8"]

