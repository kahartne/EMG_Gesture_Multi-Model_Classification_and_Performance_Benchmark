import pandas as pd
import numpy as np

df = pd.read_csv("data/test.csv")

with open("models/features.txt") as f:
    feature_cols = [line.strip() for line in f]

x = df[feature_cols].values.astype("float32")

# Apply scaling
mean = np.loadtxt("models/feature_mean.txt")
std  = np.loadtxt("models/feature_std.txt")
x = ((x - mean) / std).astype("float32")

# Apply repeat
x = np.repeat(x, repeats=3000, axis=0)

print("Final shape:", x.shape)