import argparse
import pandas as pd
import sys
from pathlib import Path
import onnxruntime as ort
import os
import time
import numpy as np

def load_model(model_path, device):
  if device == "cpu":
      providers = ["CPUExecutionProvider"]
  else:
      available = ort.get_available_providers()
      if "CUDAExecutionProvider" not in available:
          raise RuntimeError("GPU requested but CUDAExecutionProvider not available")

      providers = [("CUDAExecutionProvider", {
                "device_id": 0,
                "arena_extend_strategy": "kNextPowerOfTwo",
                "gpu_mem_limit": 2 * 1024 * 1024 * 1024,
                "cudnn_conv_algo_search": "EXHAUSTIVE",
            }),
            "CPUExecutionProvider"
        ]

  model = ort.InferenceSession(str(model_path), providers=providers)
  print("Available providers:", ort.get_available_providers())
  print("Requested providers:", providers)
  print("Session providers:", model.get_providers())
  return model

def main():
  parser = argparse.ArgumentParser()
  parser.add_argument("--model", required=True, help="log|mlp")
  parser.add_argument("--gesture", required=True, help="Path to gesture.csv")
  parser.add_argument("--out", required=True, help="Path to output predictions.csv")
  parser.add_argument("--device", choices=["cpu", "gpu"], required=True)
  parser.add_argument("--scale", type=int, default=1)
  args = parser.parse_args()

  print("=== DEBUG ===")
  print("CWD:", os.getcwd())
  print("Model path:", args.model)
  print("Gesture path:", args.gesture)
  print("================")

  # Measure total time
  start_total = time.perf_counter()

  # Measure load time
  start_load = time.perf_counter()
  model = load_model(args.model, args.device)
  end_load = time.perf_counter()
  load_time = (end_load - start_load) * 1000 # ms

  # Load [] features
  df = pd.read_csv(args.gesture)

  # Features
  with open("models/features.txt") as f:
    feature_cols = [line.strip() for line in f]

  x = df[feature_cols].values.astype("float32")

  # Normalization of Features
  mean = np.loadtxt("models/feature_mean.txt")
  std  = np.loadtxt("models/feature_std.txt")

  x = ((x - mean) / std).astype("float32")

  print("Original x shape:", x.shape)
  print("Original x sample:\n", x[:5])

  # Ensure 2D shape
  if x.ndim == 1:
    x = x.reshape(-1, 3)

  # Safe tiling
  x = np.repeat(x, repeats=args.scale, axis=0)

  print("Tiled x shape:", x.shape)
  print("Tiled x sample:\n", x[:5])
  print("Min/Max of x:", x.min(), x.max())

  # ONNX inference
  input_name = model.get_inputs()[0].name

  start_inf = time.perf_counter()

  # Run multiple times to increase workload (comment out for _ in range(runs) to just run once)
  runs = 1
  for _ in range(runs):
    outputs = model.run(None, {input_name: x})

  end_inf = time.perf_counter()

  print("Number of outputs:", len(outputs))
  for i, o in enumerate(outputs):
    print(f"Output {i} shape:", o.shape)
    print("Sample:", o[:5])

  output = outputs[0]

  print("Output shape:", output.shape)
  print("Sample:", output[:5])

  #=vvvvv=PROBLEM AREA=vvvvv=#

  if output.ndim == 2:
    # logits → normal case
    y_pred = output.argmax(axis=1)

  elif output.ndim == 1:
    # already labels → VALID for logistic / quantized
    y_pred = output.astype(int)

  else:
    raise ValueError(f"Unexpected output shape: {output.shape}")

  #=^^^^^=PROBLEM AREA=^^^^^=#

  print("Unique predictions:", np.unique(y_pred))
  print("OUTPUT SHAPE:", output.shape)
  print("OUTPUT SAMPLE:", output[:5])
  print("Using output index:", 1 if len(outputs)==2 else 0)

  inf_time = ((end_inf - start_inf) / runs) * 1000 # average per run
  total_time = ((time.perf_counter() - start_total) / runs) * 1000 # average per run

  # Write baseline predictions
  out = pd.DataFrame({"id": range(len(y_pred)), "predicted_gesture": y_pred})
  out.to_csv(args.out, index=False)
  print(f"Saved predictions to {args.out}")
  print(f"TOTAL_TIME_MS={total_time:.3f}")
  print(f"LOAD_TIME_MS={load_time:.3f}")
  print(f"INFERENCE_TIME_MS={inf_time:.3f}")

if __name__ == "__main__":
  main()