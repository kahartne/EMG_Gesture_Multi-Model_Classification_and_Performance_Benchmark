import subprocess
import time
import csv
from pathlib import Path
import re
import pandas as pd
import onnxruntime as ort
import os

MODELS = [
  ("logistic_regression.onnx", "FP32"),
  ("logistic_regression_int8.onnx", "INT8"),
  ("mlp.onnx", "FP32"),
  ("mlp_int8.onnx", "INT8"),
]

DEVICES = ["cpu"]
SCALE = 3000 # modify for workload testing (e.g. 3000) [default = 1]

if "CUDAExecutionProvider" in ort.get_available_providers():
    DEVICES.append("gpu")

DATA_PATH = "data/test.csv"
RESULTS_FILE = "results/benchmark_results.csv" # harness datapath and produce results

df_data = pd.read_csv(DATA_PATH)
NUM_SAMPLES = len(df_data) # for throughput

def run_inference(model_path, gesture_path, out_path, device, scale):
  from emg_gesture_prediction_generator import run_inference
  run_inference(model_path=f"models/{model}",
                gesture_path=DATA_PATH,
                out_path="results/predictions.csv",
                device=device,
                scale=scale)

def run_once(model, device, scale):
  result = subprocess.run([
      "python", "emg_gesture_prediction_generator.py", # or "python", "main.py"
      "--model", f"models/{model}",
      "--gesture", DATA_PATH, # currently data/test.csv (see DATA_PATH = ... above) change if needed
      "--out", "results/predictions.csv",
      "--device", device,
      "--scale", str(scale)
  ], capture_output=True, text=True, cwd=os.getcwd())

  output = result.stdout + result.stderr # show error

  total_match = re.search(r"TOTAL_TIME_MS=(\d+\.?\d*)", output)
  load_match = re.search(r"LOAD_TIME_MS=(\d+\.?\d*)", output)
  inf_match = re.search(r"INFERENCE_TIME_MS=(\d+\.?\d*)", output)

  if not (total_match and load_match and inf_match):
    raise RuntimeError(f"Failed to parse timing output:\n{output}")

  total_time = float(total_match.group(1))
  load_time = float(load_match.group(1))
  inf_time = float(inf_match.group(1))

  transfer_time = total_time - inf_time # approximate

  return total_time, load_time, inf_time, transfer_time

def benchmark():
  Path("results").mkdir(exist_ok=True)

  with open(RESULTS_FILE, "w", newline="") as f:
      writer = csv.writer(f)
      writer.writerow(["Model",
                       "Precision",
                       "Device",
                       "Total Time (ms)",
                       "Load Time (ms)",
                       "Inference Time (ms)",
                       "Transfer Time (ms)",
                       "Throughput (samples/sec)"
                      ])

      for model, precision in MODELS:
          for device in DEVICES:

            print(f"\nRunning: {model} | {precision} | {device}")

            run_once(model, device, SCALE) # warmup (ignore)

            total_times = []
            load_times = []
            inf_times = []
            transfer_times = []

            for _ in range(5):  # repeat runs
                total, load, inf, transfer = run_once(model, device, SCALE)
                total_times.append(total)
                load_times.append(load)
                inf_times.append(inf)
                transfer_times.append(transfer)

            avg_total = sum(total_times) / len(total_times)
            avg_load = sum(load_times) / len(load_times)
            avg_inf = sum(inf_times) / len(inf_times)
            avg_transfer = sum(transfer_times) / len(transfer_times)

            throughput = NUM_SAMPLES / (avg_total / 1000)

            writer.writerow([model,
                             precision,
                             device,
                             avg_total,
                             avg_load,
                             avg_inf,
                             avg_transfer,
                             throughput
                            ])
            print(f"Avg Total: {avg_total:.2f} ms | "
                  f"Inference: {avg_inf:.2f} ms | "
                  f"Transfer: {avg_transfer:.2f} ms | "
                  f"Throughput: {throughput:.2f} samples/sec")

if __name__ == "__main__":
  benchmark()